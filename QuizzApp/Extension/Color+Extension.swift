import Foundation
import UIKit

extension UIColor {
	static func primaryColor() -> UIColor {
		return UIColor(red: 88/255, green: 40/255, blue: 40/255, alpha: 1)
	}

	static func secondaryColor() -> UIColor {
		return UIColor(red: 234/255, green: 153/255, blue: 153/255, alpha: 1)
	}

}
