import Foundation
import UIKit

extension UIFont {
	static func rallomuFont(with size: CGFloat) -> UIFont? {
		return UIFont(name: "Rallomy-Regular", size: size)
	}

	static func soulRegularFont(with size: CGFloat) -> UIFont? {
		return UIFont(name: "LTSoul-Regular", size: size)
	}
}
